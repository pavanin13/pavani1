package varibles;
public class operator5 {
public static void main(String[] args) {
		System.out.println(40<<4);//positive numbers << >>> work same
		System.out.println(20>>3);
		System.out.println(20>>2);
		System.out.println(20>>>2);
		System.out.println(-20>>>2);//negative numbers >>> parity  MSB 0 
		}
}


